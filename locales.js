/* ─────────────────────────────────────────────────────────────────
   applyLocale(code)
   Replaces all [data-i18n] and [data-i18n-html] elements on the page.
   Falls back to 'en' if a key is missing in the target language.
   Saves preference to localStorage.
───────────────────────────────────────────────────────────────── */
window.applyLocale = function(code) {
  var dict = window.LOCALES[code] || window.LOCALES['en'];
  var en   = window.LOCALES['en'];

  /* text nodes */
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var val = dict[key] || en[key];
    if (val !== undefined) el.textContent = val;
  });

  /* HTML nodes (for spans/markup inside a string) */
  document.querySelectorAll('[data-i18n-html]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-html');
    var val = dict[key] || en[key];
    if (val !== undefined) el.innerHTML = val;
  });

  /* update lang button labels */
  ['lang-btn-label', 'lang-btn-m-label', 'ki-lang-lbl'].forEach(function(id) {
    var el = document.getElementById(id);
    if (el) el.textContent = code.toUpperCase();
  });

  /* update active state in all dropdowns */
  document.querySelectorAll('.lang-option, .lang-opt-m, .ki-lang-opt').forEach(function(btn) {
    btn.classList.toggle('active', btn.getAttribute('data-lang') === code);
  });

  /* persist */
  try { localStorage.setItem('salonai_lang', code); } catch(e) {}

  /* also rebuild skills section if available */
  if (window.buildSkills) window.buildSkills(code);
};

