// ── Core functions ────────────────────────────────────────────────────────────

window.applyTranslations = function(lang) {
  var t = window.T[lang] || window.T['de'];
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined && t[key] !== null) {
      el.innerHTML = t[key];
    }
  });
};

window.setLang = function(code) {
  var validCodes = Object.keys(window.LANG_META);
  var lang = validCodes.indexOf(code) !== -1 ? code : 'de';

  // Persist
  try { localStorage.setItem('salonai_lang', lang); } catch(e) {}

  // Update flag & code display
  var meta = window.LANG_META[lang];
  var flagEl = document.getElementById('lang-flag');
  var codeEl = document.getElementById('lang-code');
  if (flagEl) flagEl.textContent = meta.flag;
  if (codeEl) codeEl.textContent = meta.code;

  // Close menu
  var menu = document.getElementById('lang-menu');
  if (menu) menu.classList.remove('open');

  // Apply text translations
  window.applyTranslations(lang);

  // Rebuild skills with new language
  if (typeof window.buildSkills === 'function') {
    window.buildSkills(lang);
  }
};

window.toggleLangMenu = function() {
  var menu = document.getElementById('lang-menu');
  if (menu) menu.classList.toggle('open');
};

// Close menu on outside click
document.addEventListener('click', function(e) {
  var switcher = document.getElementById('lang-switcher');
  var menu = document.getElementById('lang-menu');
  if (menu && switcher && !switcher.contains(e.target)) {
    menu.classList.remove('open');
  }
});

